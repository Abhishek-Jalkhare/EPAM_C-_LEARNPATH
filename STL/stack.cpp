//emplace same as push
#include <iostream>
#include <stack>
using  namespace std;

int main(int argc, char *argv[]) {
    stack<int> st ;
    st.push(1);
    st.pop();
    st.size();
    st.empty();
    stack<int> st2;
    st.swap(st2); // swaps both the stack
}
